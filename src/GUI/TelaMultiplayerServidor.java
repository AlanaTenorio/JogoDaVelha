    private void iniciarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarButtonActionPerformed
        SwingWorker worker = new SwingWorker() {
            @Override
            protected Object doInBackground() throws Exception {
                conexao = servidor.accept();
                statusLabel.setText("Cliente conectado!");
                Thread.sleep(1000);
                entradaCliente = new BufferedReader(new InputStreamReader(conexao.getInputStream()));
                saidaCliente = new DataOutputStream(conexao.getOutputStream());
                statusLabel.setText("Sua vez de jogar...");
                mudarCor();
                habilitaTabuleiro();
                Thread.sleep(1000);
                return null;
            }
        };
        
        try{
            servidor = new ServerSocket(6666);
            ipServidor.setText("IP Servidor: "+InetAddress.getLocalHost().getHostAddress());
            statusLabel.setText("Esperando cliente...");
            worker.execute();
            iniciarButton.setEnabled(false);
        } catch (IOException ex){
            Logger.getLogger(TelaMultiplayerServidor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_iniciarButtonActionPerformed

